@implementation FlickrPhotosTableViewController

@synthesize photos = _photos;
@synthesize place = _place;

- (void)setPhotos:(NSArray *)photos{
    if(_photos != photos)
        _photos = photos;
    [self.tableView reloadData];
}

- (IBAction)refresh:(id)sender {
    UIActivityIndicatorView *spinner = [[UIActivityIndicatorView alloc] initWithActivityIndicatorStyle:UIActivityIndicatorViewStyleGray];
    [spinner startAnimating];
    self.navigationItem.rightBarButtonItem = [[UIBarButtonItem alloc] initWithCustomView:spinner];
    dispatch_queue_t downloadQueue = dispatch_queue_create("download photos", NULL);
    dispatch_async(downloadQueue, ^{
        NSArray *flickrPhotos = [FlickrFetcher photosInPlace:self.place maxResults:50];
        
        dispatch_async(dispatch_get_main_queue(), ^{
            [self setPhotos:flickrPhotos];
            [spinner stopAnimating];
            self.navigationItem.rightBarButtonItem = sender;
        });
    });
}

